class while1{
public static void main(String[] args){
    int n=20;
    int i=1;
    System.out.print("Even numbers are: ");
    while(i<=n){
        if(i%2==0){
           System.out.println(i); 
        }
    i++;
    }
    i=1;
    System.out.println("odd numbers are: ");
    while(i<=n){
        if(i%2!=0){
        System.out.println(i);  
        }
    i++;
    }
}
}
    
